package com.foodbucket.orderModel;


public class Order {
    private int orderId;
    private int orderYear;
    private int orderMonth;
    private int orderDay;
    private String orderState;
    private int orderCusId;

    /**
     * @return the orderId
     */
    public int getOrderId() {
        return orderId;
    }

    /**
     * @param orderId the orderId to set
     */
    public void setOrderId(int orderId) {
        this.orderId = orderId;
    }

    /**
     * @return the orderYear
     */
    public int getOrderYear() {
        return orderYear;
    }

    /**
     * @param orderYear the orderYear to set
     */
    public void setOrderYear(int orderYear) {
        this.orderYear = orderYear;
    }

    /**
     * @return the orderMonth
     */
    public int getOrderMonth() {
        return orderMonth;
    }

    /**
     * @param orderMonth the orderMonth to set
     */
    public void setOrderMonth(int orderMonth) {
        this.orderMonth = orderMonth;
    }

    /**
     * @return the orderDay
     */
    public int getOrderDay() {
        return orderDay;
    }

    /**
     * @param orderDay the orderDay to set
     */
    public void setOrderDay(int orderDay) {
        this.orderDay = orderDay;
    }

    /**
     * @return the orderState
     */
    public String getOrderState() {
        return orderState;
    }

    /**
     * @param orderState the orderState to set
     */
    public void setOrderState(String orderState) {
        this.orderState = orderState;
    }

    /**
     * @return the orderCusId
     */
    public int getOrderCusId() {
        return orderCusId;
    }

    /**
     * @param orderCusId the orderCusId to set
     */
    public void setOrderCusId(int orderCusId) {
        this.orderCusId = orderCusId;
    }
    
    
    
    
    
}
